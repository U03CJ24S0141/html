let marks = 75;
if (marks >= 90) {
  console.log("Grade A");
} else if (marks >= 75) {
  console.log("Grade B");
} else {
  console.log("Grade C or below");
}

// switch example
let day = "Friday";
switch (day) {
  case "Monday": console.log("Start"); break;
  case "Friday": console.log("Almost weekend"); break;
  default: console.log("Midweek");
}
